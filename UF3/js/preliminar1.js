let micapa=document.querySelector("#micapa");
micapa.addEventListener("click",()=>{
    micapa.style.backgroundColor="red";
})
micapa.addEventListener("dblclick",()=>{
    micapa.style.backgroundColor="yellow";
})
micapa.addEventListener("mouseenter",()=>{
    micapa.style.backgroundColor="blue";
})
micapa.addEventListener("mouseleave",()=>{
    micapa.style.backgroundColor="aqua";
})